# 사용자 질문의 자연어 표현을 온통청년 API가 실제로 인식하는 정책키워드
# 태그(plcyKywdNm)로 매핑한다. 처음엔 "일자리"/"문화"/"저축" 같은 일상 단어를
# 그대로 topic 파라미터로 보냈는데, 실제 API 응답을 대량 샘플링해보니
# plcyKywdNm은 자유 텍스트가 아니라 정부가 정한 고정된 태그 집합
# (교육지원/보조금/벤처/중소기업/주거지원/인턴/맞춤형상담서비스/장기미취업청년/
# 바우처/육아/출산/대출/해외진출/금리혜택/신용회복/청년가장/공공임대주택 등)이라,
# 일상 단어를 그대로 보내면 거의 항상 0건이 나왔다(예: "일자리"도 0건).
# 더 구체적인 표현을 먼저 확인해서, 예를 들어 "학자금"이 "교육"보다 먼저
# 매칭되도록 순서를 둔다. 확실하게 대응되는 실제 태그가 없는 단어
# (문화/여가/동아리/복지/지원금/생활비/수당/참여/권리 등)는 잘못 매핑해서
# 엉뚱한 결과를 주느니 아예 빼서, 지역 기반 검색으로 자연히 넘어가게 한다.
_TOPIC_KEYWORD_MAP: list[tuple[str, str]] = [
    ("전세", "주거지원"),
    ("월세", "주거지원"),
    ("주거", "주거지원"),
    ("주택", "주거지원"),
    ("기숙사", "주거지원"),
    ("공공임대", "공공임대주택"),
    ("장학금", "교육지원"),
    ("등록금", "교육지원"),
    ("학자금", "교육지원"),
    ("교육", "교육지원"),
    ("취업", "장기미취업청년"),
    ("일자리", "장기미취업청년"),
    ("채용", "장기미취업청년"),
    ("구직", "장기미취업청년"),
    ("해외인턴", "해외진출"),
    ("국제교류", "해외진출"),
    ("인턴", "인턴"),
    ("창업", "벤처"),
    ("사업화", "벤처"),
    ("중소기업", "중소기업"),
    ("대출", "대출"),
    ("금리", "금리혜택"),
    ("신용회복", "신용회복"),
    ("출산", "출산"),
    ("육아", "육아"),
    ("청년가장", "청년가장"),
    ("바우처", "바우처"),
    ("상담", "맞춤형상담서비스"),
]


def extract_topic_keyword(question: str) -> str | None:
    """질문 문자열에서 실제 온통청년 API 정책키워드 태그를 찾아 반환한다."""
    for trigger, tag in _TOPIC_KEYWORD_MAP:
        if trigger in question:
            return tag
    return None
